# comparison operators :
print ( 5 > 2 ) # True
print ( 5 < 3 ) # False
print ( 5 >= 3) # True
print ( 3 <= 3) # True
print ( 10 == 10 ) # True
print ( 10 == 15-5 ) # True
print ( 10 != 15 ) # True
print ( 10 != 10 ) # False

